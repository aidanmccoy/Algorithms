public class Camper {
	private int swimtime;
	private int runtime;
	private int biketime;

	private void setSwimtime(int time) {
		this.swimtime = time;
	}

	private void setRuntime(int time) {
		this.runtime = time;
	}

	private void setBiketime(int time) {
		this.biketime = time;
	}

	private int getSwimtime() {
		return this.swimtime;
	}

	private int getRuntime() {
		return this.runtime;
	}

	private int getBiketime() {
		return this.biketime;
	}

}
